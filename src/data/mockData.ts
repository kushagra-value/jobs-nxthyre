// Generate logos as colored divs with company initials
const generateLogo = (name: string, bgColor: string = 'bg-blue-500'): string => {
  const initials = name.split(' ')
    .map(word => word[0])
    .join('')
    .toUpperCase();
  
  return `${bgColor}|${initials}`;
};

